def classify_title(title: str) -> tuple[str, str]:
    """Classify a book by its title. Returns (category, subcategory)."""
    if not title:
        return DEFAULT_CATEGORY, DEFAULT_SUBCATEGORY

    title_lower = title.lower()

    for cat, subcat, *keywords in CATEGORY_RULES:
        for kw in keywords:
            if kw.lower() in title_lower:
                return cat, subcat

    return DEFAULT_CATEGORY, DEFAULT_SUBCATEGORY


